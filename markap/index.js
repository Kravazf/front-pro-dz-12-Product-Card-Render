// У додаткових матеріалах:

// Архів images.zip із зображеннями для кожного товару.
// render.png – приклад відображення покупки.
// Рендер карточки товара.pdf – приклад виконання завдання.

let cabbage = 8;
let avocado = 30;
let tomato = 10;
let grapes = 20;
let raspberry = 25;
let coconut = 50;

do {
  season = prompt('Введіть сезон (summer або winter):').replaceAll(' ', '').toLowerCase();
} while (season !== "summer" && season !== "winter");

do {
  category = prompt('Введіть категорію (vegetables або fruits):').replaceAll(' ', '').toLowerCase();
} while (category !== 'vegetables' && category !== 'fruits');

productOptions = (category === 'vegetables') ? ['cabbage', 'avocado', 'tomato'] : ['grapes', 'raspberry', 'coconut'];
do {
  product = prompt(`Введіть продукт (${productOptions.join(', ')}):`).replaceAll(' ', '').toLowerCase();
} while (productOptions.indexOf(product) === -1);

do {
  count = parseInt(prompt("Введіть кількість продукту (не менше 1):"));
} while (isNaN(count) || count < 1);

switch (product) {
  case "cabbage":
    finalPrice = cabbage * count;
    break;
  case "avocado":
    finalPrice = avocado * count;
    break;
  case "tomato":
    finalPrice = tomato * count;
    break;
  case "grapes":
    finalPrice = grapes * count;
    break;
  case "raspberry":
    finalPrice = raspberry * count;
    break;
  case "coconut":
    finalPrice = coconut * count;
    break;
}

if (season === "summer") {
  finalPrice *= 0.8;
} else {
  finalPrice *= 2;
}

document.write(`
<div class="product" align="center">
  <img src="images/${product}.svg" alt="${product}" width="100" height="100">
  <p>Selected product: <b>${product}</b></p>
  <p>Count of ${product}: <b>${count}</b></p>
  <p>Selected period: <b>${season}</b></p>
  <p>Selected category: <b>${category}</b></p>
  <p>Final sum: <b>${finalPrice} UAH</b></p>
</div>
`);