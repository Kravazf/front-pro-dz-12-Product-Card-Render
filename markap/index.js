// ВАЖЛИВО:
// Продовжуйте запитувати у користувача товари, доки слово не буде введено правильним (одним із трьох типів).
// Введене значення може бути в будь-якому регістрі та з будь-якою кількістю пробілів перед/після/всередині слова.
// Запитати кількість продуктів, які були вибрані раніше.
// ВАЖЛИВО:
// Значення має бути більше 1.
